function checkURL(inputText) {
    console.log("Validating URL", inputText);

    //this code is sourced from (as i was unsure how to validate urls): https://stackoverflow.com/questions/5717093/check-if-a-javascript-string-is-a-url
    const url = new RegExp('^(https?:\\/\\/)?'+ // protocol
    '((([a-z\\d]([a-z\\d-]*[a-z\\d])*)\\.)+[a-z]{2,}|'+ // domain name
    '((\\d{1,3}\\.){3}\\d{1,3}))'+ // OR ip (v4) address
    '(\\:\\d+)?(\\/[-a-z\\d%_.~+]*)*'+ // port and path
    '(\\?[;&a-z\\d%_.~+=-]*)?'+ // query string
    '(\\#[-a-z\\d_]*)?$','i'); // fragment locator
    return url.test(inputText);
}

export { checkURL }
